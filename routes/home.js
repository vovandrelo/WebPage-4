import express from 'express';

/* <================================================================================================================> */
/* <========================================== ОБРАБОТКА РОУТОВ ДОМАШНЕЙ СТРАНИЦЫ ==================================> */
/* <================================================================================================================> */

// Экспортируем роутер:
const homeRoutes = express.Router();

// Запрос на получение домашней страницы:
homeRoutes.get("/", (req, res) => {
    // Ответом является домашняя страница:
    res.status(200).render("pages/index.pug", {
        title: "Главная страница"
    });
});

export default homeRoutes;